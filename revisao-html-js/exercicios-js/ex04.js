// Escreva um programa que pergunte ao usuário sua idade e imprima no console se ele é maior ou menor de 
// idade de acordo com a legislação brasileira vigente.
const prompt = require("prompt-sync")();
let idade = parseInt(prompt("Digite sua idade: "))

if(idade >= 18) {
    console.log("Você é maior de idade!")
} else {
    console.log("Você é menor de idade!")
}
